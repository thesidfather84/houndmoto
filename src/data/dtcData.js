function getPRangeInfo(num) {
  for (const [min, max, system, desc] of P_RANGES) {
    if (num >= min && num <= max) return { system, desc };
  }
  return { system: "Powertrain", desc: "An OBD-II powertrain fault. Consult a qualified technician or scan for additional codes." };
}

export function getDtcInfo(rawCode) {
  if (!rawCode) return null;
  const code = rawCode.toUpperCase().trim();

  // Exact match in database
  if (dtcDatabase[code]) {
    return { code, ...dtcDatabase[code], isGeneric: false };
  }

  // Build fallback
  const letter = code[0];
  const num = parseInt(code.slice(1), 10);

  if (isNaN(num) || code.length < 5) return null;

  if (letter === "P") {
    const { system, desc } = getPRangeInfo(num);
    return {
      code,
      isGeneric: true,
      title: `${code} — ${system} Code`,
      meaning: desc,
      severity: num >= 300 && num <= 399 ? "high" : "moderate",
      canDrive: num >= 300 && num <= 399 ? "Short distances only if light is steady. Stop immediately if check engine light is flashing." : "Yes, with reduced performance — diagnose soon",
      symptoms: ["Check engine light", "May affect drivability depending on system", "Possible reduced performance or fuel economy"],
      causes: ["See diagnostic steps below — root cause varies by vehicle and sub-code"],
      diagnostics: ["Read all stored codes — companion codes often identify the exact component", "Check wiring and connectors for the affected system", "Research this specific code for your make and model using ALLDATA or a factory service manual"],
      fixes: ["Follow diagnosis to identify the specific failed component", "Check technical service bulletins (TSBs) for your vehicle — this code may have a known fix"],
      related: [],
      difficulty: "Varies",
      safety: num >= 300 && num <= 399 ? "Flashing check engine light during misfire means stop driving immediately to prevent catalytic converter damage." : "Consult a technician for safety-critical systems.",
    };
  }

  const bodyMap = {
    B: { system: "Body", desc: "A body control system fault — may affect HVAC, air bags, power windows, or other body electronics. Consult a technician for airbag-related B codes." },
    C: { system: "Chassis", desc: "A chassis control system fault — may affect ABS, stability control, traction control, or steering. Have chassis safety codes inspected promptly." },
    U: { system: "Network/Communication", desc: "A network or communication fault between vehicle modules. Check for loose module connectors, damaged CAN bus wiring, or a failed module." },
  };

  const info = bodyMap[letter];
  if (!info) return null;

  return {
    code,
    isGeneric: true,
    title: `${code} — ${info.system} Code`,
    meaning: info.desc,
    severity: letter === "C" ? "high" : "moderate",
    canDrive: letter === "C" ? "Have it inspected — chassis safety systems may be compromised" : "Yes, but address soon",
    symptoms: ["Warning light on dash", "Affected system may not operate normally"],
    causes: ["Component failure in affected system", "Wiring or connector fault", "Module communication issue"],
    diagnostics: ["Read all stored codes", "Inspect affected system wiring", "Test individual components in the affected circuit"],
    fixes: ["Replace or repair the identified component", "Repair wiring or connectors as needed"],
    related: [],
    difficulty: "Varies",
    safety: letter === "C" ? "ABS, stability, and airbag faults should be inspected by a qualified technician." : "Consult a technician for system-specific guidance.",
  };
}

